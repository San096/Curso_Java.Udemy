public class produto {
    private String name;
    private double price;


    public produto(String name , double price){
        this.name = name;
        this.price = price;
    }

    public String getName(){
        return this.name;
    }

    public double getPrice(){
        return this.price;
    }
    public void setNmae(){
        this.name = name;
    }
    public void setPrice(){
        this.price = price;
    }

   
    

}
